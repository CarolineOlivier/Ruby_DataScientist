              # Q3: Combien y-a-t'il de handle contenant 5 caractères (le @ ne compte pas pour un caractère)
              # Méthode qui compte les handles avec 5 caractères (sans le @)
              def compteur_handles_5_caracteres(handlesArray)
                compteur_5_caracteres = 0 # Initialisation du compteur à 0.
                # Il servira à compter combien de handles respectent la condition.
                handlesArray.each do |handle| # utilisation de la méthode each pour parcourir chaque élément de la collection handles.
                  if handle.length - 1 == 5 # Vérification que la longueur du handle (moins le '@') est égale à 5
                    # == est un opérateur de comparaison :
                    # Si les deux valeurs sont égales, cela retourne true (vrai).
                    # Si les valeurs sont différentes, cela retourne false (faux).
                    compteur_5_caracteres += 1 # Prends la valeur actuelle de compteur_5_caracteres, lui ajoute 1,
                    # puis assigne le résultat à compteur_5_caracteres
                  end
                end
                compteur_5_caracteres # Retourne le nombre de handles avec 5 caractères
              end
              
              # Q4 : Combien commencent par une majuscule (première lettre juste après le @) ?
              def compteur_handles_majuscule(handlesArray)
                compteur = 0 # Initialisation d'un compteur à 0 pour compter le nombre de handles qui commencent par une majuscule.
                handlesArray.each do |handle|
                  # Passe en revue, parcourt (= "itère") chaque élément du tableau un par un pour effectuer des opérations sur eux.
                  if handle[1] =~ /[A-Z]/
                    #  =~ /[A-Z]/  expression régulière qui vérifie si le caractère est une lettre majuscule. Si c'est une majuscule, cela retourne true.
                    compteur += 1 # Incrémentation du compteur si la condition est remplie (si la première lettre après le @ est une majuscule).
                  end
                end
                compteur # Retourner le nombre de handles commençant par une majuscule
              end
              
